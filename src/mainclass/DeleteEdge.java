    void validateInput() throws Invalid
    {
        if(from.getText().isEmpty() || to.getText().isEmpty())
            throw new Invalid("Fill all the fields");
    }
    
    int deleteEdge(String f,String t)
    {
        if(hm.containsKey(f))
        {
            ArrayList<Graph>al=hm.get(f);
            
            int i;
            
            for(i=0;i<al.size();i++)
            {
                Graph g=al.get(i);
                
                if(g.getName().equals(t))
                {
                    al.remove(i);
                    return 1;
                }
            }
            
            return -1;
        }
        else
            return -1;
    }
    
